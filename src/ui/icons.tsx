type IconProps = {
  name: string;
};

export const Icon: FunctionComponent<IconProps> = ({ name }) => {
  switch (name) {
    case "IconCommandLine":
      return <IconCommandLine />;
    case "IconClipboard":
      return <IconClipboard />;
    case "IconStack":
      return <IconStack />;
    case "IconLink":
      return <IconLink />;
    case "IconExternalLink":
      return <IconExternalLink />;
    case "IconBell":
      return <IconBell />;
    case "IconImage":
      return <IconImage />;
    case "IconReturnKey":
      return <IconReturnKey />;
    case "IconSun":
      return <IconSun />;
    case "IconMoon":
      return <IconMoon />;
    case "IconCommandKey":
      return <IconCommandKey />;
    default:
      return null;
  }
};

